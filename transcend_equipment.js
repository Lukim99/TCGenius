function weapon(name, atk, desc, owner) {
    return {
        name, rarity: '초월', desc, transcend: true,
        stat: { atk }, plusStat: {},
        upgrade: WEAPON_ATK_UPGRADE_RATIOS.map(ratio => ({ stat: { atk: Math.round(atk * ratio) } })),
        requireMainCard: owner ? [MAIN_CARD_IDS[owner]] : undefined
    };
}

function armor(name, slot, def, hp, set, desc, mythic) {
    const data = {
        name, rarity: mythic ? '신화' : '초월', desc, set, armorSlot: slot,
        stat: { def, hp }, plusStat: {},
        upgrade: Array.from({ length: 15 }, (_, i) => ({
            stat: {
                def: Math.round(def * ARMOR_DEF_UPGRADE_RATIOS[i]),
                hp: Math.round(hp * ARMOR_HP_UPGRADE_RATIOS[i])
            }
        }))
    };
    if (!mythic) data.transcend = true;
    else data.mythic = true;
    return data;
}

function accessory(name, atk, set, desc, mythic) {
    const data = {
        name, rarity: mythic ? '신화' : '초월', desc, set,
        stat: { atk }, plusStat: {},
        upgrade: [], mythic: !!mythic
    };
    if (!mythic) data.transcend = true;
    return data;
}

function support(name, minAtk, maxAtk, set, desc, mythic) {
    const data = {
        name, rarity: mythic ? '신화' : '초월', desc, set,
        stat: { atk: minAtk }, statRange: { atk: maxAtk - minAtk }, plusStat: {},
        upgrade: [], mythic: !!mythic
    };
    if (!mythic) data.transcend = true;
    return data;
}

